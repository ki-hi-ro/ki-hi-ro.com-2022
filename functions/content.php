function kihiro_topic_tag_ids() {
    static $topic_tag_ids = null;

    if (null !== $topic_tag_ids) {
        return $topic_tag_ids;
    }

    $topic_tag_ids = array(
        'technology' => array(),
        'curation'   => array(),
    );
    $terms = get_terms(
        array(
            'taxonomy'   => 'post_tag',
            'hide_empty' => false,
        )
    );

    if (is_wp_error($terms)) {
        return $topic_tag_ids;
    }

    $definitions = kihiro_topic_definitions();

    foreach ($terms as $term) {
        foreach (array('technology', 'curation') as $topic) {
            if (in_array($term->name, $definitions[$topic]['tag_names'], true)) {
                $topic_tag_ids[$topic][] = (int) $term->term_id;
            }
        }
    }

    return $topic_tag_ids;
}

function kihiro_apply_topic_query($query, $topic) {
    $tag_ids   = kihiro_topic_tag_ids();
    $tax_query = array('relation' => 'AND');

    if ('curation' === $topic) {
        $tax_query[] = array(
            'taxonomy' => 'post_tag',
            'field'    => 'term_id',
            'terms'    => $tag_ids['curation'],
        );
    } elseif ('technology' === $topic) {
        $tax_query[] = array(
            'taxonomy' => 'post_tag',
            'field'    => 'term_id',
            'terms'    => $tag_ids['technology'],
        );
        if ($tag_ids['curation']) {
            $tax_query[] = array(
                'taxonomy' => 'post_tag',
                'field'    => 'term_id',
                'terms'    => $tag_ids['curation'],
                'operator' => 'NOT IN',
            );
        }
    } else {
        $excluded_tag_ids = array_merge($tag_ids['curation'], $tag_ids['technology']);
        if ($excluded_tag_ids) {
            $tax_query[] = array(
                'taxonomy' => 'post_tag',
                'field'    => 'term_id',
                'terms'    => $excluded_tag_ids,
                'operator' => 'NOT IN',
            );
        } else {
            // With no classification tags, every post belongs to philosophy.
            return;
        }
    }

    $query->set('tax_query', $tax_query);
}

function kihiro_configure_main_query($query) {
    if (is_admin() || !$query->is_main_query()) {
        return;
    }

    if ($query->is_home()) {
        $query->set('posts_per_page', 3);
        $query->set('orderby', 'modified');
        $query->set('order', 'DESC');

        $topic = kihiro_current_topic();
        if ($topic) {
            kihiro_apply_topic_query($query, $topic);
        }
    } elseif ($query->is_search() || $query->is_tag() || $query->is_date()) {
        $query->set('posts_per_page', 10);
    } else {
        return;
    }

    $excluded_ids = array_unique(
        array_merge(
            (array) $query->get('post__not_in'),
            kihiro_excluded_post_ids()
        )
    );

    $query->set('post__not_in', $excluded_ids);
}
add_action('pre_get_posts', 'kihiro_configure_main_query');

function kihiro_get_archive_months() {
    $archives = get_transient('kihiro_archive_months');

    if (false !== $archives && is_array($archives)) {
        return $archives;
    }

    global $wpdb;

    $rows = $wpdb->get_results(
        "SELECT DISTINCT YEAR(post_date) AS archive_year, MONTH(post_date) AS archive_month
        FROM {$wpdb->posts}
        WHERE post_type = 'post' AND post_status = 'publish'
        ORDER BY post_date DESC"
    );
    $archives = array();

    foreach ($rows as $row) {
        $year = (int) $row->archive_year;
        $archives[$year][] = (int) $row->archive_month;
    }

    set_transient('kihiro_archive_months', $archives, 12 * HOUR_IN_SECONDS);

    return $archives;
}

function kihiro_clear_archive_months_cache() {
    delete_transient('kihiro_archive_months');
}
add_action('save_post_post', 'kihiro_clear_archive_months_cache');
