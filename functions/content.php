/**
 * Prevent Easy Table of Contents from mistaking a literal selector example
 * for an already-rendered table of contents.
 *
 * Easy Table of Contents checks the entire content for the plain string
 * `ez-toc-container`. Posts that discuss the plugin can therefore lose their
 * automatically inserted TOC. Mask the literal before the plugin's priority
 * 100 content filter, then restore it immediately afterwards.
 */
function kihiro_mask_ez_toc_container_literal($content) {
    if (!is_singular('post') || false === strpos($content, 'ez-toc-container')) {
        return $content;
    }

    // Preserve a real TOC generated by a shortcode or another integration.
    if (preg_match('/<[^>]+\bid=(["\'])ez-toc-container\1/i', $content)) {
        return $content;
    }

    return str_replace(
        'ez-toc-container',
        'ez-toc-kihiro-literal-container',
        $content
    );
}
add_filter('the_content', 'kihiro_mask_ez_toc_container_literal', 99);

function kihiro_restore_ez_toc_container_literal($content) {
    return str_replace(
        'ez-toc-kihiro-literal-container',
        'ez-toc-container',
        $content
    );
}
add_filter('the_content', 'kihiro_restore_ez_toc_container_literal', 101);

function kihiro_custom_excerpt($length = 170) {
    $post = get_post();

    if (!$post instanceof WP_Post) {
        return '';
    }

    $content = wp_strip_all_tags($post->post_excerpt);

    if ('' === $content) {
        $content = strip_shortcodes($post->post_content);
        $content = html_entity_decode(wp_strip_all_tags($content), ENT_QUOTES, 'UTF-8');
    }

    if (mb_strlen($content, 'UTF-8') > $length) {
        return mb_substr($content, 0, $length, 'UTF-8') . '...';
    }

    return $content;
}

function custom_excerpt_pc($length = 170) {
    return kihiro_custom_excerpt($length);
}

function custom_excerpt_sp($length = 100) {
    return kihiro_custom_excerpt($length);
}

function my_tags_in_cat($cat_id) {
    $post_ids = get_objects_in_term((int) $cat_id, 'category');

    if (is_wp_error($post_ids) || empty($post_ids)) {
        return array();
    }

    return wp_get_object_terms($post_ids, 'post_tag');
}

